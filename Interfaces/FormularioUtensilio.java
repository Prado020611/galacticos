    public void cargarUtensilio(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id");
        modelo.addColumn("nombre");
        modelo.addColumn("cantidad");
        modelo.addColumn("tipo");
        modelo.addColumn("cocina");
        

       try{
            Conexion conexion = new Conexion();
            Connection con = conexion.con;
            String sql = "SELECT id, nombre, cantidad, tipo, cocina FROM Utensilio";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet datos = ps.executeQuery();
            
            while(datos.next()){
                int id = datos.getInt("id");
                String nombre = datos.getString("nombre");
                String tipo = datos.getString("tipo");
                String cocina = datos.getString("cocina");
                
                Utensilio uten = new Utensilio(id, nombre, tipo, cocina);
                
                    modelo.addRow(new Object []{
                    uten.getId(),
                    uten.getNombre(),
                    uten.getTipo(),
                    uten.getCocina(),
                    });


                tabla_uten.setModel(modelo);
                
            }
            //datos.close();
            //ps.close();
            //con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al cargar los datos" +e.getMessage());
        }
    }

